package baseball;

import java.util.List;

public class ScoreDetect {

    /**
     * get strike count by number
     * @param answer
     * @param numbers
     * @return
     */
    public int getStrikeCount (
            List<Integer> answer,
            List<Integer> numbers
    ) {
        if (numbers.size() != 3)
            return -1;
        return 0;
    }

    public int getBallCount (
            List<Integer> answer,
            List<Integer> numbers
    ) {
        if (numbers.size() != 3)
            return -1;
        return 0;
    }
}
